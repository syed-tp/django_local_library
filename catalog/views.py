class BookCreateView(CreateView):
    model = Book
    form_class = BookForm
    template_name = 'catalog/book_form.html'
    success_url = reverse_lazy('books')  # Redirect to the list of books after creation

    def form_valid(self, form):
        if not self.request.user.has_perm('catalog.can_add_book'):
            return self.handle_no_permission()
        return super().form_valid(form)
    
